export const currencyCodes = uniq(
  currencies.map((currency) => currency.code || currency.currency),
);

export const currencyCodesOptions = currencyCodes.map((currency) => ({
  label: currency,
  value: currency,
}));

// Alternative structure - object indexed by currency code for quick lookup
export const currencyByCode = currencies.reduce<
  Record<
    string,
    Array<{
      country: string;
      currency: string;
      numeric: number | null;
    }>
  >
>((acc, curr) => {
  if (curr.code) {
    if (!acc[curr.code]) {
      acc[curr.code] = [];
    }
    acc[curr.code].push({
      country: curr.country,
      currency: curr.currency,
      numeric: curr.numeric,
    });
  }
  return acc;
}, {});
